/** Look up a word's meaning + picture, ignoring case and punctuation.
    Understands common word forms: cats -> cat, hopped -> hop, stories ->
    story, Sam's -> sam, running -> run. */
export function lookup(word: string): DictEntry | null {
  const key = word.toLowerCase().replace(/[^a-z]/g, "");
  if (DICTIONARY[key]) return DICTIONARY[key];
  const tries: string[] = [];
  if (key.endsWith("ies")) tries.push(key.slice(0, -3) + "y");
  if (key.endsWith("ied")) tries.push(key.slice(0, -3) + "y");
  if (key.endsWith("ves")) tries.push(key.slice(0, -3) + "f");
  if (key.endsWith("es")) tries.push(key.slice(0, -2));
  if (key.endsWith("s")) tries.push(key.slice(0, -1));
  if (key.endsWith("ed")) tries.push(key.slice(0, -2), key.slice(0, -1), key.slice(0, -3));
  if (key.endsWith("ing"))
    tries.push(key.slice(0, -3), key.slice(0, -3) + "e", key.slice(0, -4));
  if (key.endsWith("est")) tries.push(key.slice(0, -3), key.slice(0, -4));
  if (key.endsWith("er")) tries.push(key.slice(0, -2), key.slice(0, -1), key.slice(0, -3));
  if (key.endsWith("ly")) tries.push(key.slice(0, -2));
  for (const t of tries) {
    if (t.length > 1 && DICTIONARY[t]) return DICTIONARY[t];
  }
  return null;
}

/** Best-effort picture + meaning + grammar for ANY word, working out a
    sensible guess from how the word is built. Used so that every word a
    child taps shows a picture, even rare ones not in the dictionary. */
function deriveFallback(word: string): DictEntry {
  const key = word.toLowerCase().replace(/[^a-z]/g, "");
  const ends = (s: string) => key.endsWith(s) && key.length > s.length + 1;

  // Adverb: how something is done.
  if (ends("ly"))
    return {
      emoji: "⚡",
      meaning: `Tells us how something is done — in a "${key.slice(0, -2)}" way.`,
      pos: "adverb",
    };
  // Abstract naming words (ideas and feelings you cannot touch).
  if (
    ends("tion") || ends("sion") || ends("ment") || ends("ness") ||
    ends("ity") || ends("ance") || ends("ence") || ends("ship") ||
    ends("hood") || ends("dom") || ends("ism") || ends("ery")
  )
    return {
      emoji: "💭",
      meaning: "A naming word for an idea or feeling you cannot touch.",
      pos: "noun",
    };
  // A person who does something.
  if (ends("ist") || ends("ian") || ends("eer"))
    return {
      emoji: "🧑",
      meaning: "A naming word — usually a person who does something.",
      pos: "noun",
    };
  // Describing words.
  if (
    ends("ful") || ends("ous") || ends("ive") || ends("ical") ||
    ends("ish") || ends("less") || ends("able") || ends("ible") ||
    ends("est")
  )
    return {
      emoji: "🎨",
      meaning: "A describing word that tells us more about something.",
      pos: "adjective",
    };
  // Doing words.
  if (ends("ing"))
    return {
      emoji: "🏃",
      meaning: "A doing word — an action that is happening.",
      pos: "verb",
    };
  if (ends("ed"))
    return {
      emoji: "✅",
      meaning: "A doing word — an action that already happened.",
      pos: "verb",
    };
  // Catch-all: treat as a naming word.
  return {
    emoji: "📖",
    meaning: "A word from our story. Look at the picture, then sound it out!",
    pos: "noun",
  };
}

/** Always returns a picture + meaning + grammar for a word — never null.
    Tries the real dictionary first, then a smart best guess. */
export function describe(word: string): DictEntry {
  return lookup(word) ?? deriveFallback(word);
}
